package model;

public class ThongKe {
    private int tongSoPhong;
    private int soPhongTrong;
    private int soPhongDaThue;
    private int tongKhachThue;
    private double doanhThu;

    public int getTongSoPhong() {
        return tongSoPhong;
    }

    public void setTongSoPhong(int tongSoPhong) {
        this.tongSoPhong = tongSoPhong;
    }

    public int getSoPhongTrong() {
        return soPhongTrong;
    }

    public void setSoPhongTrong(int soPhongTrong) {
        this.soPhongTrong = soPhongTrong;
    }

    public int getSoPhongDaThue() {
        return soPhongDaThue;
    }

    public void setSoPhongDaThue(int soPhongDaThue) {
        this.soPhongDaThue = soPhongDaThue;
    }

    public int getTongKhachThue() {
        return tongKhachThue;
    }

    public void setTongKhachThue(int tongKhachThue) {
        this.tongKhachThue = tongKhachThue;
    }

    public double getDoanhThu() {
        return doanhThu;
    }

    public void setDoanhThu(double doanhThu) {
        this.doanhThu = doanhThu;
    }
}
